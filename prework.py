# This script rates a basketball players free throw shooting percentage.

player_name = 'Stephen Curry'
ftshooting_percent = 71

if ftshooting_percent >= 88:
    print(player_name,'great shooting with', ftshooting_percent,'%.')
    
elif ftshooting_percent >= 70:
    print(player_name,'solid shooting with', ftshooting_percent,'%.')

else:
    print(player_name,'go practice!')
